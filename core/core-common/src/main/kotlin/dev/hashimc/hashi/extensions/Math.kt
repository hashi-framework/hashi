package dev.hashimc.hashi.extensions


fun lengthSquared(xDistance: Double, yDistance: Double): Double {
    return xDistance * xDistance + yDistance * yDistance
}

fun square(value: Float): Float {
    return value * value
}

fun square(value: Double): Double {
    return value * value
}

fun square(value: Int): Int {
    return value * value
}

fun square(value: Long): Long {
    return value * value
}